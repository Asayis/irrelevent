package com.syntax.class03;

public class AdditionOrConcentrationTask {

	public static void main(String[] args) {
		
		
		// Question 1
		
		
		int num=200;
		num=num+500;
		
		System.out.println(num); //700
		
		num=num+200;
		
		System.out.println(num); //900
		
		//Question 2
		
		int num1=200;
		num1=num1-60;
		
		System.out.println(num1); //140
		
		//Question 3
		
		int cakePiece=11;
		cakePiece/=4;
		
		System.out.println(cakePiece); //2
		
		//Question 4
		
		int cake=24;
		cake%=7;
		
		System.out.println(cake); //3
	
	}

}
